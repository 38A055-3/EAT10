window.i18nTranslations = translations;
window.currentLang = localStorage.getItem('eat10_lang') || 'ja';

window.t = function(key) {
    if (window.i18nTranslations[window.currentLang] && window.i18nTranslations[window.currentLang][key]) {
        return window.i18nTranslations[window.currentLang][key];
    }
    // Fallback to Japanese if translation is missing
    return window.i18nTranslations['ja'][key] || key;
};

window.updateAllUIText = function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'INPUT' && el.type === 'text') {
            el.placeholder = window.t(key);
            // Optionally update value if it matches placeholder (e.g., initial state)
            if (el.value === "プレイヤー名" || el.value === "Player Name" || el.value === "玩家名称" || el.value === "플레이어 이름") {
                el.value = window.t(key);
            }
        } else {
            const prefix = el.getAttribute('data-i18n-prefix') || '';
            el.innerHTML = prefix + window.t(key);
        }
    });
};
